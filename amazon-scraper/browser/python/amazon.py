def parse_rufus(html: str, question: str) -> RufusAnswer:
    """parse Rufus's answer panel for the assistant reply and any cited products"""
    sel = Selector(text=html)
    answer_text = "\n".join(
        t.strip()
        for t in sel.css("[data-testid=rufus-message-assistant] ::text, [data-rufus-message-role=assistant] ::text").getall()
        if t.strip()
    )
    product_refs: List[ProductRef] = []
    for card in sel.css("[data-testid=rufus-message-assistant] a[href*='/dp/'], [data-rufus-message-role=assistant] a[href*='/dp/']"):
        href = card.css("::attr(href)").get()
        if not href:
            continue
        url = urljoin("https://www.amazon.com", href).split("?")[0]
        asin_match = re.findall(r"/dp/([A-Z0-9]{10})", url)
        product_refs.append(
            {
                "asin": asin_match[0] if asin_match else "",
                "title": "".join(card.css("::text").getall()).strip(),
                "url": url,
            }
        )
    return {
        "question": question,
        "answer_text": answer_text,
        "product_refs": product_refs,
    }

async def scrape_rufus(url: str, question: str) -> List[RufusAnswer]:
    """ask Rufus a product question on an Amazon product page and parse the answer"""
    url = url.split("/ref=")[0]
    log.info(f"asking Rufus about {url}: {question!r}")
    html = await _ask_rufus(url, question)
    answer = parse_rufus(html, question)
    log.info(f"Rufus answered with {len(answer['product_refs'])} product refs")
    return [answer]

def to_dict(obj: Any) -> Any:
    """Recursively convert TypedDicts/lists to plain dicts/lists for JSON output."""
    if isinstance(obj, dict):
        return {k: to_dict(v) for k, v in obj.items()}
    if isinstance(obj, list):
        return [to_dict(x) for x in obj]
    return obj

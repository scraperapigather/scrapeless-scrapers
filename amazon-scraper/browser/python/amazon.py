def _client() -> Scrapeless:
    if not os.environ.get("SCRAPELESS_API_KEY") and not os.environ.get("SCRAPELESS_KEY"):
        raise RuntimeError(
            "SCRAPELESS_API_KEY env var not set. Sign up at https://app.scrapeless.com"
        )
    if not os.environ.get("SCRAPELESS_API_KEY") and os.environ.get("SCRAPELESS_KEY"):
        os.environ["SCRAPELESS_API_KEY"] = os.environ["SCRAPELESS_KEY"]
    return Scrapeless()

def _add_or_replace_url_parameters(url: str, **params) -> str:
    """adds url parameters or replaces them with new values"""
    parsed_url = urlparse(url)
    query_params = dict(parse_qsl(parsed_url.query))
    query_params.update({k: str(v) for k, v in params.items()})
    updated_url = parsed_url._replace(query=urlencode(query_params))
    return urlunparse(updated_url)

async def _fetch_rendered_html(
    url: str,
    ready_selector: Optional[str] = None,
    *,
    proxy_country: str = DEFAULT_PROXY_COUNTRY,
    retries: int = 1,
) -> str:
    """Discover → extract: mint a session, goto, wait for stable marker, return HTML.

    Includes an Amazon-specific retry around the "Continue shopping" interstitial.
    """
    last_error: Exception | None = None
    for attempt in range(retries + 1):
        client = _client()
        session = client.browser.create(
            ICreateBrowser(proxy_country=proxy_country, session_ttl=DEFAULT_SESSION_TTL)
        )
        ws = session.browser_ws_endpoint
        async with async_playwright() as pw:
            browser = await pw.chromium.connect_over_cdp(ws)
            try:
                page = await browser.new_page()
                await page.goto(url, wait_until="domcontentloaded", timeout=45000)
                # Handle "Continue shopping" interstitial
                try:
                    btn = await page.query_selector("button:has-text('Continue shopping')")
                    if btn:
                        await btn.click()
                        await page.wait_for_load_state("domcontentloaded", timeout=15000)
                except Exception:
                    pass
                if ready_selector:
                    try:
                        await page.wait_for_selector(ready_selector, timeout=15000)
                    except Exception as e:
                        log.warning("wait_for_selector {!r} failed (continuing): {}", ready_selector, e)
                html = await page.content()
                if html and "Continue shopping" not in html[:5000]:
                    return html
                last_error = RuntimeError("interstitial / empty HTML")
            except Exception as e:  # noqa: BLE001
                last_error = e
                log.warning("attempt {} failed: {}", attempt + 1, e)
            finally:
                try:
                    await browser.close()
                except Exception:
                    pass
    # Last resort: even an empty HTML is better than nothing for parse-debug
    if last_error and "html" in dir(last_error):
        return ""
    raise RuntimeError(f"giving up after {retries + 1} attempts: {last_error}")

async def _ask_rufus(
    url: str,
    question: str,
    *,
    proxy_country: str = DEFAULT_PROXY_COUNTRY,
) -> str:
    """Open Rufus on a product page, type a question, and return the rendered answer HTML.

    Rufus is interactive — unlike the read-only `_fetch_rendered_html`, this opens the
    assistant panel, types the question, sends it, and waits for the streamed reply to settle.
    """
    client = _client()
    session = client.browser.create(
        ICreateBrowser(proxy_country=proxy_country, session_ttl=DEFAULT_SESSION_TTL)
    )
    ws = session.browser_ws_endpoint
    async with async_playwright() as pw:
        browser = await pw.chromium.connect_over_cdp(ws)
        try:
            page = await browser.new_page()
            await page.goto(url, wait_until="domcontentloaded", timeout=45000)
            try:
                btn = await page.query_selector("button:has-text('Continue shopping')")
                if btn:
                    await btn.click()
                    await page.wait_for_load_state("domcontentloaded", timeout=15000)
            except Exception:
                pass
            # Open the Rufus panel
            try:
                launcher = await page.query_selector("[data-csa-c-content-id=rufus-launcher], #nav-rufus-disco, [aria-label*='Rufus']")
                if launcher:
                    await launcher.click()
            except Exception:
                pass
            # Type the question and send it
            await page.wait_for_selector("textarea[data-testid=rufus-text-input], textarea[name=rufus-input]", timeout=15000)
            box = await page.query_selector("textarea[data-testid=rufus-text-input], textarea[name=rufus-input]")
            await box.fill(question)
            await page.keyboard.press("Enter")
            # Wait for the streamed answer to settle
            await page.wait_for_selector("[data-testid=rufus-message-assistant], [data-rufus-message-role=assistant]", timeout=30000)
            await page.wait_for_timeout(2500)
            return await page.content()
        finally:
            try:
                await browser.close()
            except Exception:
                pass


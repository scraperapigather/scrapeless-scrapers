def parse_product(html: str) -> Product:
    """parse Amazon's product page for essential product data"""
    images: List[str] = []
    if color_images := re.findall(r"colorImages':.*'initial':\s*(\[.+?\])},\n", html):
        try:
            images = [img["large"] for img in json.loads(color_images[0])]
        except Exception:
            pass
    if image_gallery := re.findall(r"imageGalleryData'\s*:\s*(\[.+\]),\n", html):
        try:
            images = [img["mainUrl"] for img in json.loads(image_gallery[0])]
        except Exception:
            pass

    sel = Selector(text=html)
    parsed: Product = {
        "name": sel.css("#productTitle::text").get("").strip(),
        "asin": sel.css("input[name=ASIN]::attr(value)").get("").strip(),
        "style": "".join(sel.css("[id^=inline-twister-expanded-dimension-text] ::text").getall()).strip(),
        "description": "\n".join(sel.css("#productDescription p span ::text").getall()).strip(),
        "stars": sel.css("i[data-hook=average-star-rating] ::text").get("").strip(),
        "rating_count": sel.css("span[data-hook=total-review-count] ::text").get("").strip(),
        "features": [value.strip() for value in sel.css("#feature-bullets li ::text").getall() if value.strip()],
        "images": images,
        "info_table": {},
    }
    info_table: Dict[str, str] = {}
    for row in sel.css("#productDetails_detailBullets_sections1 tr"):
        label = row.css("th::text").get("").strip()
        value = row.css("td::text").get("").strip()
        if not value:
            value = row.css("td span::text").get("").strip()
        if label:
            info_table[label] = value
    info_table["Customer Reviews"] = sel.xpath("//td[div[@id='averageCustomerReviews']]//span[@class='a-icon-alt']/text()").get()
    rank = sel.xpath("//tr[th[text()=' Best Sellers Rank ']]//td//text()").getall()
    info_table["Best Sellers Rank"] = " ".join([text.strip() for text in rank if text.strip()])
    parsed["info_table"] = info_table
    return parsed

async def scrape_product(url: str) -> List[Product]:
    """scrape Amazon.com product (and any sibling variants)"""
    url = url.split("/ref=")[0]
    asin = url.split("/dp/")[-1].rstrip("/")
    log.info(f"scraping product {url}")
    html = await _fetch_rendered_html(
        url,
        ready_selector="#productDetails_detailBullets_sections1 tr",
    )
    variants: List[Product] = [parse_product(html)]

    # If product has variants — scrape all of them
    _variation_data = re.findall(r'dimensionValuesDisplayData"\s*:\s*({.+?}),\n', html)
    if _variation_data:
        try:
            variant_asins = [a for a in json.loads(_variation_data[0]) if a != asin]
            log.info(f"scraping {len(variant_asins)} variants: {variant_asins}")
            for variant_asin in variant_asins:
                variant_url = f"https://www.amazon.com/dp/{variant_asin}"
                v_html = await _fetch_rendered_html(
                    variant_url,
                    ready_selector="#productDetails_detailBullets_sections1 tr",
                )
                variants.append(parse_product(v_html))
        except Exception as e:
            log.warning("failed to parse variants: {}", e)
    return variants


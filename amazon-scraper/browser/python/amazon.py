def parse_search(html: str, base_url: str) -> List[ProductPreview]:
    """Parse search result page for product previews"""
    sel = Selector(text=html)
    previews: List[ProductPreview] = []
    product_boxes = sel.css("div.s-result-item[data-component-type=s-search-result]")
    for box in product_boxes:
        href = box.css("div>a::attr(href)").get()
        if not href:
            continue
        url = urljoin(base_url, href).split("?")[0]
        if "/slredirect/" in url:
            continue
        rating = box.xpath(".//div[@data-cy='reviews-block']//a[contains(@aria-label, 'out of')]/@aria-label").re_first(r"(\d+\.*\d*) out")
        rating_count = box.xpath(".//div[@data-cy='reviews-block']//a[contains(@aria-label, 'ratings')]/@aria-label").get()
        previews.append(
            {
                "url": url,
                "title": box.css("div>a>h2::attr(aria-label)").get(),
                "price": box.css(".a-price[data-a-size=xl] .a-offscreen::text").get(),
                "real_price": box.xpath(".//div[@data-cy='secondary-offer-recipe']//span[contains(@class, 'a-color-base') and contains(text(), '$')]/text()").get(),
                "rating": float(rating) if rating else None,
                "rating_count": int(rating_count.replace(",", "").replace(" ratings", "")) if rating_count else None,
            }
        )
    log.info(f"parsed {len(previews)} product previews from {base_url}")
    return previews

async def scrape_search(url: str, max_pages: Optional[int] = None) -> List[ProductPreview]:
    """Scrape amazon search pages product previews"""
    log.info(f"{url}: scraping first page")
    first_html = await _fetch_rendered_html(
        url,
        ready_selector="div.s-result-item[data-component-type=s-search-result]",
    )
    results = parse_search(first_html, url)

    # Find total pages from the paging meta
    sel = Selector(text=first_html)
    paging_meta = sel.xpath("//*[@cel_widget_id='UPPER-RESULT_INFO_BAR-0']//span/text()").get() or ""
    total_pages = 1
    try:
        total_results = int(re.findall(r"(?:over\s+)?([\d,]+)\s+results", paging_meta)[0].replace(",", ""))
        results_per_page = int(re.findall(r"\d+-(\d+)", paging_meta)[0])
        total_pages = math.ceil(total_results / results_per_page)
    except (IndexError, ValueError):
        log.warning("could not parse paging meta: {!r}", paging_meta)

    if max_pages and total_pages > max_pages:
        total_pages = max_pages

    log.info(f"{url}: scraping {total_pages} pages")
    for page in range(2, total_pages + 1):
        page_url = _add_or_replace_url_parameters(url, page=page)
        page_html = await _fetch_rendered_html(
            page_url,
            ready_selector="div.s-result-item[data-component-type=s-search-result]",
        )
        results.extend(parse_search(page_html, page_url))

    log.info(f"{url}: found total of {len(results)} product previews")
    return results

